Example = 'Топинамбур' #// В переменную example.
print(Example [0])     # первый символ этой строки.
print(Example [9])     # последний символ этой строки (используя отрицательный индекс).
print(Example [5:10])  # вторую половину этой строки
print(Example [::-1])  # это слово наоборот
print(Example [1:11:2]) # каждый второй символ этой строки.